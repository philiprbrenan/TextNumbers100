#-------------------------------------------------------------------------------
# Translate
#-------------------------------------------------------------------------------

sub translate($$)
 {my ($language, $number) = @_;
  my $l = &translations;
  my $n = $l->{$language};
  $n or confess "No translation for language: $language";
  my $i = $number+0;
  $n->{$i}
 }

#-------------------------------------------------------------------------------
# Languages
#-------------------------------------------------------------------------------

sub languages
 {my $l = &translations;
  sort keys %{&translations};
 }

